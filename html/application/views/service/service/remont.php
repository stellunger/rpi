					12. Проверка отсутствия возможных отложений в сборном резервуаре КНС/ДНС, мусора в корзине (при наличии отложений или мусора сообщить заказчику для принятия последним мер).<br>
					13. Проверка рабочего состояния шкафа управления с протяжкой контактных групп соединений.</p>

