					8. Проверка правильности подключения насосов с замерами потребляемого электрического тока.<br>
					9. Проверка и очистка датчиков контроля уровня.<br>
					10. Очистка наружных поверхностей обратных клапанов и задвижек, смазка штревелей задвижек (при необходимости).<br>
